# Python - Sort Lists

# Sort List Alphanumerically
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print("1. ",thislist)

thislist = [100, 50, 65, 82, 23]
thislist.sort()
print("2. ",thislist)

# Sort Descending
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort(reverse = True)
print("3. ",thislist)

# Reverse Order

thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.reverse()
print("4. ",thislist)