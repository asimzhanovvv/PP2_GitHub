# Copy Lists

# Copy method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print("1. ", mylist)

# List method
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print("2. ", mylist)

# Slicing method
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print("3. ", mylist)