# Python - Modify Strings

# Upper Case
a = "Hello, World!"
print(a.upper()) # returns "HELLO, WORLD!"

# Lower Case
a = "Hello, World!"
print(a.lower()) # returns "hello, world!"

# Remove Whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# Replace String
a = "Hello, World!"
print(a.replace("H", "J")) # returns "Jello, World!"

# Split String
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']